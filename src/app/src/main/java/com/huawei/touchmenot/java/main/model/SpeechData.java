package com.huawei.touchmenot.java.main.model;

import com.huawei.hms.site.api.model.Site;

import java.io.Serializable;

public class SpeechData implements Serializable {
    private int requestType = 1;
    private String request;
    private String response;
    private Site siteResponse;

    public String getRequest() {
        return request;
    }

    public void setRequest(String request) {
        this.request = request;
    }

    public String getResponse() {
        return response;
    }

    public void setResponse(String response) {
        this.response = response;
    }

    public int getRequestType() {
        return requestType;
    }

    public void setRequestType(int requestType) {
        this.requestType = requestType;
    }

    public Site getSiteResponse() {
        return siteResponse;
    }

    public void setSiteResponse(Site siteResponse) {
        this.siteResponse = siteResponse;
    }
}